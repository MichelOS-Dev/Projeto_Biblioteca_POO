    private void jButtonPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPesquisarActionPerformed
        mod.setPesquisa(jTextFieldPesquisa.getText());
        BeansUsuario model = dao.buscaUsuario(mod);
        if(jTextFieldPesquisa.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Busca vazia!");
        }else if(model.getCodUsuario() != 0){
            jTextFieldUsuario.setText(model.getNome());
            jComboBoxTipo.setSelectedItem(model.getTipo());
            jPasswordFieldSenha.setText(model.getSenha());
            jPasswordFieldConfirmarSenha.setText(model.getSenha());
            jTextFieldID.setText(String.valueOf(model.getCodUsuario()));
            
            jButtonAtualizar.setEnabled(true);
            jButtonDeletar.setEnabled(true);
            jButtonCancelar.setEnabled(true);
            
            
            preencherTabela("select * from administradores where nome ilike'%"+mod.getPesquisa()+"%'");
            
        }else{
            jButtonAtualizar.setEnabled(false);
            jButtonDeletar.setEnabled(false);
            jButtonCancelar.setEnabled(false);
            preencherTabela("select * from administradores order by nome");
        }
        
        
        
    }//GEN-LAST:event_jButtonPesquisarActionPerformed

