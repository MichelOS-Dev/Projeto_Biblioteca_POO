    private void jTableUsuarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableUsuarioMouseClicked
        String usuario = ""+jTableUsuario.getValueAt(jTableUsuario.getSelectedRow(),1);
       //JOptionPane.showMessageDialog(null,nome_aluno);
       conex.conexao();
       conex.executeSql("select * from administradores where nome='"+usuario+"'");
       try{      
                conex.rs.first();
                jTextFieldID.setText(String.valueOf(conex.rs.getInt("codigo")));
                jTextFieldUsuario.setText(conex.rs.getString("nome"));
                jPasswordFieldSenha.setText(conex.rs.getString("senha"));
                jPasswordFieldConfirmarSenha.setText(conex.rs.getString("senha"));
                jComboBoxTipo.setSelectedItem(conex.rs.getString("tipo"));
                
       
       }catch(SQLException ex){
           JOptionPane.showMessageDialog(null,"Erro ao selecionar os dados!\nErro: "+ex);
           //Erro: org.postgresql.util.PSQLException:ResultSet não está posicionado corretamente, talvez você precise chamar next.
       }
       
       conex.desconecta();
        jButtonNovo.setEnabled(false);
        jTextFieldUsuario.setEnabled(false);
        jPasswordFieldConfirmarSenha.setEnabled(false);
        jPasswordFieldSenha.setEnabled(false);
        jComboBoxTipo.setEnabled(false);
        jButtonAtualizar.setEnabled(true);
        jButtonDeletar.setEnabled(true);
        jButtonCancelar.setEnabled(true);
        jButtonSalvar.setEnabled(false);
    }//GEN-LAST:event_jTableUsuarioMouseClicked

    public void preencherTabela(String Sql){
    
        ArrayList dados = new ArrayList();
        String [] colunas = new String []{"ID","Usuário","Senha","Tipo","Dica da senha"};
        conex.conexao();
        conex.executeSql(Sql);
        try{
            conex.rs.first();
            do{
                dados.add(new Object[]{conex.rs.getInt("codigo"),conex.rs.getString("nome"),conex.rs.getString("senha"),conex.rs.getString("tipo"),conex.rs.getString("dicasenha")});
            }while(conex.rs.next());
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"Busque por outro Usuário para preencher a tabela!");
        }
        
        ModeloTabela modelo = new ModeloTabela(dados,colunas); 
        
        jTableUsuario.setModel(modelo);
        jTableUsuario.getColumnModel().getColumn(0).setPreferredWidth(35);
        jTableUsuario.getColumnModel().getColumn(0).setResizable(false);
        jTableUsuario.getColumnModel().getColumn(1).setPreferredWidth(180);
        jTableUsuario.getColumnModel().getColumn(1).setResizable(false);
        jTableUsuario.getColumnModel().getColumn(2).setPreferredWidth(100);
        jTableUsuario.getColumnModel().getColumn(2).setResizable(false);
        jTableUsuario.getColumnModel().getColumn(3).setPreferredWidth(130);
        jTableUsuario.getColumnModel().getColumn(3).setResizable(false);
        jTableUsuario.getColumnModel().getColumn(4).setPreferredWidth(300);
        jTableUsuario.getColumnModel().getColumn(4).setResizable(false);
        
        
        jTableUsuario.getTableHeader().setReorderingAllowed(false);
        jTableUsuario.setAutoResizeMode(jTableUsuario.AUTO_RESIZE_OFF);
        jTableUsuario.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        
        conex.desconecta();
    
    }
    
    
